
    var curWid = $(window).innerWidth();
    var curHeight= $(window).innerHeight();


    // 视频
    function videoDo(isAuto){
        jwplayer.key = "J0+IRhB3+LyO0fw2I+2qT2Df8HVdPabwmJVeDWFFoplmVxFF5uw6ZlnPNXo=";
        jwplayer("video_player").setup({
            width: '80%', //视频区域大小设置
            height: curHeight*0.7,
            // image: '../imgs/video-bg.jpg',  //视频区域图片
            autostart: isAuto,
            file: '1.mp4',
            dock: false,
            repeat: true
        });
    }
    $(".video_panel").css("margin-top",curHeight*0.15);

    $(".see_video").click(function(){
        $(".alert_video").fadeIn();
        videoDo(true);
    });

    $(".close_video").click(function(){
        videoDo(false);
        $(".alert_video").fadeOut();
    });
	
	
	


